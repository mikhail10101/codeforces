void solve() {
    int n; cin >> n;
    unordered_set<int> pool;
    for (int i = 0; i < n; i++) {
        int x; cin >> x;
        pool.insert(x);
    }

    /*
    Measure how many different levels there are
    For each level except the last, increase and smash needs to happen
    For the last level, only increase will be enough
    */
    cout << 2 * pool.size() - 1;
}

