template <typename T>
void printVec(vector<T> vec) {
    for (T v: vec) {
        cout << v << " ";
    } cout << endl;
}

void solve() {
    int n; cin >> n;
    for (int diff = 1; diff <= n - 1; diff++) {
        for (int i = 0; i < n - diff; i++) {
            int j = i + diff;
            cout << i + 1 << " " << j + 1 << endl;
            cout.flush();
            fflush(stdout);
            int x; cin >> x;
        }
    }
}

bool multiple = true;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t = 1;
    if (multiple) cin >> t;
    while (t--) {
        solve();
        cout << endl;
    }

}