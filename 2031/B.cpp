int int_log(int n, int base) {
    if (base < 2) throw invalid_argument("Base must be >= 2");
    if (n < 1)    throw invalid_argument("n must be >= 1");
    int result = 0;
    while (n >= base) {
        n /= base;
        result++;
    }
    return result;
}

int find_conseq(int n) {
    int root = (int)sqrt(n * 2);
    while (root * (root + 1) > n * 2) root--;
    return root;
}

void solve() {
    /*
    Only determine if possible

    Operation: swap sticks that differ by at most 1
    For 1 to move to another location, it can only keep swapping with 2
    In the case of 4 3 2 1, after 1 swap, 4 3 1 2, 1 cannot move anywhere

    To fix an array, we then choose to fix the extremities and move inwards

    2 1 3 4
    For 1 to make it to the first index, 2 has to be there
    If 2 is already there, that means that its fixed after the swap
    
    There occurs a pattern in diving the array into segments of 2
    Start either at the left or at the right
    */
    int n; cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) cin >> a[i];

    for (int i = 0; i < n; i++) {
        if (a[i] == i + 1) continue;
        if (i + 1 < n && a[i + 1] == i + 1 && a[i] == i + 2) {
            swap(a[i], a[i + 1]);
        } else {
            cout << NO; return;
        }
    }

    cout << YES;
}

