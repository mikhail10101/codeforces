int int_log(int n, int base) {
    if (base < 2) throw invalid_argument("Base must be >= 2");
    if (n < 1)    throw invalid_argument("n must be >= 1");
    int result = 0;
    while (n >= base) {
        n /= base;
        result++;
    }
    return result;
}

int find_conseq(int n) {
    int root = (int)sqrt(n * 2);
    while (root * (root + 1) > n * 2) root--;
    return root;
}

void solve() {
    // Cut the cell in half at each point in time until it reaches one
    int a, b, n, m; cin >> n >> m >> a >> b; a--; b--;
    int x, y;
    x = min(a + 1, n - a);
    y = min(b + 1, m - b);
    int res = min(
        (int)ceil(log2(x)) + (int)ceil(log2(m)),
        (int)ceil(log2(n)) + (int)ceil(log2(y))
    ) + 1;
    cout << res;
}

