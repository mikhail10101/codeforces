using namespace std;

const int INF = 1e9 + 11;

template <typename T>
void printVec(vector<T> vec) {
    for (T v: vec) {
        cout << v << " ";
    } cout << endl;
}

void solve() {
    int n; cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }

    vector<int> res;
    int mini = INF;
    for (int i = 0; i < n; i++) {
        if (a[i] < mini) {
            mini = a[i];
        } else {
            res.push_back(i + 1);
        }
    }

    cout << res.size() << endl;
    printVec(res);
}

bool multiple = true;

int main() {
    int t = 1;
    if (multiple) cin >> t;
    while (t--) {
        solve();
        cout << endl;
    }

}